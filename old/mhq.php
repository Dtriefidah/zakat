   <h3 class="page-header">Mushabaqoh Hifdzil Qur’an (MHQ) JABODETABEK-SERCIL</h3>
   <div class="row">
   <div class="col-lg-12">
	 <ol class="breadcrumb">
<p>Assalamualaikum wr wb<br><br>
Puji syukur kami panjatkan kehadirat Allah SWT , shalawat serta salam semoga senantiasa selalu tercurah kepada Rasulullah SAW beserta keluarga nya, serta para sahabat nya & kepada kita semuanya, umat NYA yang senantiasa mendapat syafaat di yaumil akhir .
Alhamdulillah pada hari minggu tepat nya tanggal 14 Mei 2017 / 17 Sya’ban 1438 H
Griya yatim dan dhuafa menggelar event lomba MHQ( Mushabaqoh Hifdzil Qur’an ) Se JABODETABEK-SERANG-CILEGON yg di langsungkan di Buperta Jambore Cibubur
dengan tema “Dengan Alqur’an Kita Raih Kesuksesan Dunia Dan Akhirat”
Pada event kali ini Seluruh Perwakilan cabang Griya Yatim & Dhuafa Jabodetabek sercil meng ikutsertakan anak asuhnya utk mengikuti lomba tersebut.
Tujuan di adakan event ini adalah untuk menyambung silahturahmi & memotivasi seluruh anak asuh Griya Yatim & Dhuafa agar senantiasa selalu mencintai Al Qur’an juga gemar dalam menghafal nya & mengamalkan isi kandungan Al Qur’an .
Dan akhir nya lolos lah beberapa anak asuh yg menjadi juara dalam event lomba MHQ tersebut,di antara salah satu nya adalah Ananda Muhammad Fatih Al Ghozy / Ahmad juara per – 1 Lomba MHQ katagori 30 tingkat SD / MI perwakilan dari Asrama Griya Yatim & Dhuafa Cab. Deplu kreo .
Semoga yang belum mendapat juara tetap SEMANGAT…Aamiin…
</p>

