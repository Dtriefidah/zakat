	  <b>2009</b>
<br>Berawal dari rasa galau beberapa founding father yayasan GYD melihat kondisi anak-anak yang terpaksa putus sekolah atau tidak sekolah sama sekali karena harus bekerja untuk menyambung hidupnya di daerah kampung Dadap, pemukiman kumuh persis ditengah-tengah megahnya  perumahan Bumi Serpong Damai. Setelah beberapa kali mengadakan pertemuan, dibentuklah lembaga sosial yang concern pada masalah sosial khususnya anak-anak. Dengan menempati sebuah rumah di Jl. Magnolia 1 Sektor 1.2 BSD yang digunakan juga sebagai asrama yatim dan dhuafa terbentuklah organisasi sosial yang bernama Yayasan Griya Yatim dan Dhuafa.

Pada awal berdirinya, GYD dengan 6 orang karyawan menampung 9 orang anak yang tinggal diasrama dan membina sekitar 15-an anak yang semuanya berasal dari kampung Dadap. Karena dukungan masyarakat yang terus meluas mendorong dilakukannya pengelolaan organisasi ini lebih baik dirintislah program beasiswa pendidikan yatim dan dhuafa, santunan kesehatan, layanan donasi barang layak pakai dan lain-lain.

Animo masyarakat pada perlunya organisasi kemanusiaan ternyata cukup besar. Masyarakat memandang penting misi sosial ini diteruskan bahkan untuk kiprah yang lebih luas. Hanya berselang beberapa bulan, tepatnya bulan Agustus 2009 asrama kedua di Jl. Elang Raya – Bintaro Jaya dibuka. Pada akhir tahun 2009 GYD telah membina lebih dari 100 anak asuh.

<br><br><b>2010</b><br>
Pertumbuhan asrama meningkat. Kantor pelayanan dibuka didaerah Bintaro. Ekspansi mulai melebar ke Jakarta dan Bekasi dengan dibukanya asrama ketiga di Cibubur – Jakarta Timur dan asrama keempat di Kranggan – Bekasi. Dimulainya pembangunan sistem Teknologi Informasi untuk peningkatan mutu pelayanan. Hampir seluruh kantor cabang telah tersambung secara online. Website www.griyayatim.com dirilis dan disempurnakan, menggantikan alamat situs sebelumnya di www.griyayatim.org.

Menjelang akhir tahun 2010, regenerasi puncak pimpinan diestafetkan dari Adi Prabowo beralih ke Haryono. Babak sejarah baru dimulai. GYD melakukan serangkaian adaptasi dan perubahan terkait visi, misi dan value yang menjadi budaya di GYD. Pembelajaran untuk menjadi organisasi yang amanah dan profesional terus dilakukan, salah satunya dengan penguatan program-program peningkatan kualitas sumber daya manusia melalui pelatihan, training, seminar dan lain-lain.

Pada akhir tahun 2010 GYD membina lebih dari 800 binaan yang terdiri dari anak yatim dan dhuafa, janda dan lansia serta mengasuh 50an anak yang tinggal diseluruh asrama yatim dan dhuafanya.

<br><br><b>2011</b><br>
Implementasi program GYD mulai difokuskan hingga mengerucut pada enam induk yaitu Pendidikan, Sosial, Pemberdayaan, Kemanusiaan, Lingkungan dan Wakaf. Daerah yang ada disekitar asrama GYD difokuskan untuk penyaluran yang terintegrasi dibidang pendidikan, sosial, kesehatan, pelatihan keterampilan dan pemberdayaan ekonomi secara terpadu. Dengan bantuan koordinator mustahik sebagai pendamping, KBA (Komunitas berbasis asrama) menjadi pusat penyaluran program sehingga lebih terukur dan terkontrol.

Pada peringatan Milad kedua tanggal 9 Juni 2011, Griya Yatim dan Dhuafa melaunching logo dan identitas barunya menggantikan logo sebelumnya. “Dengan keyakinan kuat untuk bisa memberikan manfaat yang semakin besar, GYD berdaya upaya untuk menjadi organisasi terdepan di indonesia yang dapat menghantarkan anak yatim dan dhuafa meraih masa depannya yang lebih baik.”

<br><br><b>2012</b><br>

Atas inovasi yang dilakukan dalam pola mengasuh dan memberdayakan anak yatim dan dhuafa, GYD mendapat pengakuan dari Museum Rekor Indonesia (MURI) sebagai lembaga sosial pertama di dunia yang menggunakan kartu ATM dalam menyalurkan bantuan kepada penerima manfaat-nya.

Sebagai lembaga yang mengusung misi amanah dan profesional, atas inisiatif sendiri GYD juga telah diaudit oleh institusi akuntan publik dan pada audit perdananya ini GYD berhasil memperoleh predikat “Wajar Tanpa Pengecualian”.

Kami bertekad agar ditahun ini keberadaan GYD dapat dirasakan oleh semakin banyak orang Indonesia termasuk dengan pembukaan jaringan atau asrama dan kantor pelayanan di 10 propinsi. “Dengan keyakinan kuat untuk bisa memberikan manfaat yang semakin besar, GYD berdaya upaya untuk menjadi organisasi terdepan di indonesia yang dapat menghantarkan anak yatim dan dhuafa meraih masa depannya yang lebih baik.”

